    private void jDisplay_inKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jDisplay_inKeyPressed
       int keyCode = evt.getKeyCode();
      if(keyCode == KeyEvent.VK_ENTER){
          String str = jDisplay_in.getText();
       FormulaElement f = new FormulaElement() {};
       FormulaElement f2 = f.parseFormula(str,0,0,'x');           
       String answer = Float.toString((float) f2.evaluate());
       jDisplay_out.setText(answer);
      }
      if(keyCode == KeyEvent.VK_BACK_SPACE){
           jDisplay_in.setText (jDisplay_in.getText ().substring (0, jDisplay_in.getText ().length () - 1));
          
      }
       if(keyCode == KeyEvent.VK_1){
            String Enternumber = jDisplay_in.getText() + "1";
        jDisplay_in.setText(Enternumber);;
          
      }
      
    }//GEN-LAST:event_jDisplay_inKeyPressed

