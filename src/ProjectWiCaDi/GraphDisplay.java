 private XYDataset createDataset( ) {
     
        String s = formula_display.getText();
        char var = v_to1.getText().charAt(0);
        int start = Integer.valueOf(v_to.getText());
        int end = Integer.valueOf(v_to2.getText());
        
        final XYSeries xydataset = new XYSeries( s );
        for(int i=start; i<=end;i++){
            FormulaElement f = new FormulaElement() {};
            FormulaElement f2 = f.parseFormula(s,0,0,var); 
            f2.setVariableValue(String.valueOf(var), i);
            if(this.flag1 == true){
                f2.setVariableValue(vname1, Double.valueOf(vvalue1));
                if(this.flag2 == true){
                    f2.setVariableValue(vname2, Double.valueOf(vvalue2));
                    if(this.flag3 == true){
                        f2.setVariableValue(vname3, Double.valueOf(vvalue3));
                    }
                }
            }
           double answer = f2.evaluate();
           xydataset.add( i ,answer ); 
           st.append(i+",");
           int ans = (int) Math.round(answer);
           st.append(ans);
           st.append("\n");
      }              
      
                
      dataset.addSeries( xydataset );  
      return dataset;
   }
     
   
