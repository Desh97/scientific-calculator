    private void find_areaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_find_areaActionPerformed
        int upperbound = Integer.valueOf(v_to.getText());
        int lowerbound = Integer.valueOf(v_to2.getText());
        
        //add variable name here
        
        char var = v_to1.getText().charAt(0);
        
        String str = "∫("+formula_display.getText()+")d"+var;
        FormulaElement f = new FormulaElement() {};
        FormulaElement f2 = f.parseFormula(str, upperbound, lowerbound, var);
       
        f2.setVariableValue("e", 2.718);
        f2.setVariableValue("π", 3.141);    
       
        if(this.flag1 == true){
            f2.setVariableValue(vname1, Double.valueOf(vvalue1));
            if(this.flag2 == true){
                f2.setVariableValue(vname2, Double.valueOf(vvalue2));
                if(this.flag2 == true){
                    f2.setVariableValue(vname3, Double.valueOf(vvalue3));
                }
            }
        }
        
        String answer = Float.toString((float) f2.evaluate());
        double ans = Double.valueOf(answer);
        if(ans<0){
            ans=ans*-1;
            answer = String.valueOf(ans);
        }
        v_to3.setText(answer);
    }//GEN-LAST:event_find_areaActionPerformed

